Console macro	string, args

		; .Print method -----------------------------------------------------
		if strcmp('\0','write')
		
				move.w	sr, -(sp)

				if narg>2
						inform	2,"Too many arguments for method ""Print"" of ""Console"""
				endc

				@stack_data: = 0				; Bitfield corresponding to values types pushed onto
				@stack_data_bitcnt: = 0 		; Number of bits pushed into
				@stack_data_alloc: = 0			; Number of bytes allocated on the stack of arguments

				if narg=2
						rept narg(args)

								; Save data size to the internal bitfield that later gets pushed on to the linked stack, ...
								; ... also load primary stack with the actual values
								if instr('\args','.b')=1		; -- size = .b
										@stack_data: = (@stack_data<<2) | %10	; push code 10 to the bitfield
										@stack_data_bitcnt: = @stack_data_bitcnt + 2
										@stack_data_alloc: = @stack_data_alloc + 2

										subq.w	#2, sp					; if data is byte, allocate 2 bytes anyways to keep shifts even
										move\args\,1(sp)				; write the actual value to the high byte

								elseif instr('\args','.w')=1	; -- size = .w
										@stack_data: = (@stack_data<<1) | %0	; push code 0 to the bitfield
										@stack_data_bitcnt: = @stack_data_bitcnt + 1
										@stack_data_alloc: = @stack_data_alloc + 2

										move\args\,-(sp)				; push the actual value to the stack

								elseif instr('\args','.l')=1	; -- size = .l
										@stack_data: = (@stack_data<<2) | %11	; push code 11 to the bitfield
										@stack_data_bitcnt: = @stack_data_bitcnt + 2
										@stack_data_alloc: = @stack_data_alloc + 4

										move\args\,-(sp)				; push the actual value to the stack
								endif

								shift	args					; load next argument
						endr
								
												move.l	#@stack_data<<(32-@stack_data_bitcnt), -(sp)	; Push arguemnts types data
												@stack_data_alloc: = @stack_data_alloc + 4

				endc

				pea 	@string\@(pc)														; Push pointer to the string to the stack
				pea 	@stack_data_alloc+4(sp) 											; Push pointer to the beginning of the arguments buffer

				; Push internal stack sizes data bitfield to the linked stack, ...
				; ... align its content to the MSB

				trap	#2								; Call 'Console.Write' routine
				bra.w	@string_end\@					; Jump over the string

		@string\@:
				dc.b	\string, 0
				even
		@string_end\@:

						move.l	(sp)+, sp												; Restore stack to its previous state
						move.w	(sp)+, sr

		; .Print method -----------------------------------------------------
		elseif strcmp('\0','setxy')

				if narg>1
						inform	2,"Too many arguments for method ""SetXY"" of ""Console"""

				endc

				link	a4, #-4
				
				move.w	\1, -(a4)
				shift	1
				move.w	\1, -(a4)

				trap	#1

				addq.w	#4, a4
				unlk	a4

		; .Fail method ------------------------------------------------------
		elseif strcmp('\0','fail')
		
				if narg>1
						inform	2,"Too many arguments for method ""Fail"" of ""Console"""

				elseif narg=1
						move	sr, -(sp)
						jsr 	$B00.w							; ### ### ###
						dc.b	\string,0						; text
						dc.b	0								; extended stack frame
						even

				else
						trap	#0								; force error exception

				endc

		; Unknown method ----------------------------------------------------
		else
				inform	2,"""\0"" isn't a member of ""Console"""
		endc

		endm

